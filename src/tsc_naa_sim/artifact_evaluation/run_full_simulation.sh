#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR=$(cd -- "$(dirname -- "${BASH_SOURCE[0]}")" && pwd)
WORKLOAD_CSV="$SCRIPT_DIR/config/workloads.csv"

PRODUCER_TARGETS=(fig11a fig15 fig18 fig20)
ANALYSIS_TARGETS=(fig11a table3 fig15 fig16 fig17 fig19 fig18 fig20)

usage() {
    cat <<'EOF'
Usage: ./run_full_simulation.sh [workload] [options]

With no workload, run all workloads in the reuse-friendly order documented in
README.md, then invoke run_full_rendering.sh. With a workload selector, run
every applicable figure/table run.sh for that workload; paper-facing analyze.py
renderers are intentionally left until all workload-wise runs have completed.

Workload may be a key, label, or ham_name from config/workloads.csv.

Options forwarded to every run.sh:
  --num-threads N
  --memory-budget-gib N
  --cache-dir DIR
  --num-shots N
  --quration-results-dir DIR  Quration prerequisite root (default: quration/results)
  --force
  --dry-run
  -h, --help
EOF
}

workload_selector=""
quration_results_dir=""
dry_run=false
forward_args=()

while (($#)); do
    case "$1" in
        -h|--help)
            usage
            exit 0
            ;;
        --num-threads|--memory-budget-gib|--cache-dir|--num-shots|--quration-results-dir)
            if (($# < 2)); then
                echo "error: $1 requires a value" >&2
                exit 2
            fi
            if [[ "$1" == "--quration-results-dir" ]]; then
                quration_results_dir=$2
            else
                forward_args+=("$1" "$2")
            fi
            shift 2
            ;;
        --force)
            forward_args+=("$1")
            shift
            ;;
        --dry-run)
            dry_run=true
            forward_args+=("$1")
            shift
            ;;
        --*)
            echo "error: unknown option: $1" >&2
            usage >&2
            exit 2
            ;;
        *)
            if [[ -n "$workload_selector" ]]; then
                echo "error: specify at most one workload" >&2
                usage >&2
                exit 2
            fi
            workload_selector=$1
            shift
            ;;
    esac
done

# The simulation analyses for Fig. 17/19 need Quration CSVs. Use the results
# generated by quration/run.sh unless the caller explicitly selects another root.
if [[ -z "$quration_results_dir" ]]; then
    quration_results_dir=${AE_QURATION_RESULTS_DIR:-"$SCRIPT_DIR/quration/results"}
fi
forward_args+=(--quration-results-dir "$quration_results_dir")

workload_args=()
fig19_enabled=true
if [[ -n "$workload_selector" ]]; then
    workload_record=$(awk -F, -v selector="$workload_selector" '
        NR > 1 && ($1 == selector || $2 == selector || $3 == selector) {
            print $1 "," $8
            exit
        }
    ' "$WORKLOAD_CSV")
    if [[ -z "$workload_record" ]]; then
        echo "error: unknown workload selector: $workload_selector" >&2
        exit 2
    fi
    workload_key=${workload_record%%,*}
    fig19_flag=${workload_record##*,}
    workload_args=(--workload "$workload_key")
    if [[ "$fig19_flag" != "1" ]]; then
        fig19_enabled=false
    fi
fi

run_stage() {
    local stage=$1
    shift
    local target
    for target in "$@"; do
        if [[ "$target" == "fig19" && "$fig19_enabled" == false ]]; then
            echo "[skip] $target is not defined for workload $workload_selector"
            continue
        fi
        echo "[full-simulation] target=$target stage=$stage${workload_selector:+ workload=$workload_selector}"
        "$SCRIPT_DIR/$target/run.sh" \
            --stage "$stage" \
            "${workload_args[@]}" \
            "${forward_args[@]}"
    done
}

cd "$SCRIPT_DIR"
export MPLBACKEND=${MPLBACKEND:-Agg}

if [[ -z "$workload_selector" ]]; then
    run_stage compilation "${PRODUCER_TARGETS[@]}"
    run_stage execution "${PRODUCER_TARGETS[@]}"
    run_stage analysis "${ANALYSIS_TARGETS[@]}"
    rendering_args=(--quration-results-dir "$quration_results_dir")
    if [[ "$dry_run" == true ]]; then
        rendering_args+=(--dry-run)
    fi
    "$SCRIPT_DIR/run_full_rendering.sh" "${rendering_args[@]}"
else
    # Consumer targets reuse producer results, so the same producer-first order
    # avoids repeatedly loading the QASM merely to rediscover cache hits.
    run_stage compilation "${PRODUCER_TARGETS[@]}"
    run_stage execution "${PRODUCER_TARGETS[@]}"
    run_stage analysis "${ANALYSIS_TARGETS[@]}"
fi
